// ①整数を格納する変数を宣言して、値を代入して出力してください。
void main(){
  // 整数を格納する変数を宣言
  int a = 3;
  int b = 5;
  
  print(a+b);
  print(a-b);
  print(a/b);
  print(a*b);

//②文字列を格納する変数を宣言して、値を代入して出力してください。
  const o = "HELLO Dart";
  
  print(o);
  
//③小数を格納する変数を宣言して、値を代入して出力してください。
  double d;
  double e;
  
  d = 0.2;
  e = 0.6;
  
  print(d+e);
  print(d-e);
  print(d/e);
  print(d*e);
}
